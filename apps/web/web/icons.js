export function iconHTML(name) {
  return icons[name] || "";
}

export function iconEl(name) {
  const span = document.createElement("span");
  span.className = "icon";
  span.setAttribute("aria-hidden", "true");
  span.innerHTML = icons[name] || "";
  return span;
}

// Auto-hydrate any element with `data-icon="<name>"` on load:
//   <span class="icon" data-icon="send"></span>      → inline SVG
//   <h2 data-icon="brain">메모리</h2>                 → SVG prepended
//   <button data-icon="plus">새 대화</button>          → SVG prepended
function hydrate() {
  const nodes = document.querySelectorAll("[data-icon]");
  for (const node of nodes) {
    const name = node.getAttribute("data-icon");
    if (!name || !icons[name]) continue;

    if (node.classList.contains("icon")) {
      node.innerHTML = icons[name];
      continue;
    }
    // Prepend an icon span next to the existing text content.
    const existing = node.querySelector(".icon[data-hydrated]");
    if (existing) continue;
    const span = document.createElement("span");
    span.className = "icon";
    span.setAttribute("data-hydrated", "");
    span.setAttribute("aria-hidden", "true");
    span.innerHTML = icons[name];
    node.prepend(span);
  }
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", hydrate);
} else {
  hydrate();
}
